    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed

        if ("".equals(Name.getText()) || "".equals(Contact.getText()) || "".equals(Add.getText())) {
            JOptionPane.showMessageDialog(null, "Enter Details", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (!"".equals(Name.getText()) && !"".equals(Contact.getText()) && !"".equals(Add.getText())) {
            String gender = (String) Gen.getSelectedItem();
            try {
                Connection Con = ConnectionProvider.getcon();
                Statement st = Con.createStatement();
                String query = "insert into buyer values('" + Name.getText() + "' , '" + Contact.getText() + "' , '" + Email.getText() + "' , '" + Add.getText() + "' , '"
                        + gender + "') ";
                st.executeUpdate(query);

                JOptionPane.showMessageDialog(null, "Buyer Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                Reset();
                fetch();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Mobile Number is already Exists.");
            }
        }

    }//GEN-LAST:event_saveActionPerformed


    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String gender = (String) Gen.getSelectedItem();
        try {
            Connection con = ConnectionProvider.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("update buyer set Name='" + Name.getText() + "' , ContactNo='" + Contact.getText() + "' , Email='" + Email.getText() + "', Address='" + Add.getText() + "', Gender='" + gender + "' where ContactNo='" + Contact.getText() + "'   ");

            JOptionPane.showMessageDialog(null, "Buyer Updated", "Success", JOptionPane.INFORMATION_MESSAGE);

            Reset();
            fetch();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_updateActionPerformed

