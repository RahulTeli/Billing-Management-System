    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed

        String as = (String) Act.getSelectedItem();

        try {
            Connection con = ConnectionProvider.getcon();
            Statement st = con.createStatement();

            st.executeUpdate("update Item set  P_Name='" + PName.getText() + "' , P_Price='" + PPrice.getText() + "' , Description='" + PDesc.getText() + "' , Active_Status='" + as + "' where PID= '" + Pid.getText() + "' ");

            JOptionPane.showMessageDialog(null, "Item Updated", "Success", JOptionPane.INFORMATION_MESSAGE);

            Reset();
            fetch();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_updateActionPerformed

