    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed

        if ("".equals(PName.getText()) || "".equals(PPrice.getText()) || "".equals(PDesc.getText())) {
            JOptionPane.showMessageDialog(null, "Enter Valid Details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!"".equals(PName.getText()) && !"".equals(PPrice.getText()) && !"".equals(PDesc.getText())) {
            String as = (String) Act.getSelectedItem();

            try {
                Connection con = ConnectionProvider.getcon();
                Statement st = con.createStatement();
                String query = "insert into Item values('" + lab.getText() + "','" + PName.getText() + "' , '" + PPrice.getText() + "' , '" + PDesc.getText() + "' , '" + as + "' ) ";
                st.executeUpdate(query);

                JOptionPane.showMessageDialog(null, "Item Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                pidl();
                Reset();
                fetch();
                
            } catch (Exception e) {
                //JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_saveActionPerformed
